package nl.denhaag.rest.monitor.folder;

public class PathList {
	
	String pathname;
	String parentpathname;
	String id;
	String parentid;
	/**
	 * @return the pathname
	 */
	public String getPathname() {
		return pathname;
	}
	/**
	 * @param pathname the pathname to set
	 */
	public void setPathname(String pathname) {
		this.pathname = pathname;
	}
	/**
	 * @return the parentpathname
	 */
	public String getParentpathname() {
		return parentpathname;
	}
	/**
	 * @param parentpathname the parentpathname to set
	 */
	public void setParentpathname(String parentpathname) {
		this.parentpathname = parentpathname;
	}
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the parentid
	 */
	public String getParentid() {
		return parentid;
	}
	/**
	 * @param parentid the parentid to set
	 */
	public void setParentid(String parentid) {
		this.parentid = parentid;
	}
	
	

}
